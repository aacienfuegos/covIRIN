CGroundArea::CGroundArea(const char* pch_name) : CGeometry(pch_name)
{
	for ( int i = 0 ; i < strlen(pch_name) ; i++ )
	{
		m_sName[i] = pch_name[i];
	}
	m_dCenter.x=0.0;
	m_dCenter.y=0.0;
	m_fIntRadius=0.0;
	m_fExtRadius=0.0;
	m_fGrey=1.0;
}

